// Maps vehicle type strings from Firestore to silhouette keys
export function getVehicleSilhouette(vehicleType = "") {
  const type = vehicleType.toLowerCase();

  if (type.includes("truck") || type.includes("pickup")) return silhouettes.truck;
  if (type.includes("large") || type.includes("full")) return silhouettes.large_suv;
  if (type.includes("suv") || type.includes("crossover") || type.includes("4x4")) return silhouettes.midsize_suv;
  if (type.includes("sedan") || type.includes("coupe") || type.includes("hatchback") || type.includes("wagon")) return silhouettes.sedan;

  return silhouettes.sedan; // default fallback
}

